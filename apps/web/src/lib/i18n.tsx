type TranslationKey = keyof typeof dictionaries.vi

type LanguageContextValue = {
  language: AppLanguage
  setLanguage: (language: AppLanguage) => void
  toggleLanguage: () => void
  t: (key: TranslationKey, params?: Record<string, string | number>) => string
}

const LanguageContext = React.createContext<LanguageContextValue | null>(null)

function readInitialLanguage(): AppLanguage {
  if (typeof window === "undefined") return "vi"
  const stored = window.localStorage.getItem(STORAGE_KEY)
  return stored === "en" || stored === "vi" ? stored : "vi"
}

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = React.useState<AppLanguage>(readInitialLanguage)

  const setLanguage = React.useCallback((nextLanguage: AppLanguage) => {
    setLanguageState(nextLanguage)
    window.localStorage.setItem(STORAGE_KEY, nextLanguage)
    document.documentElement.lang = nextLanguage
  }, [])

  React.useEffect(() => {
    document.documentElement.lang = language
    window.localStorage.setItem(STORAGE_KEY, language)
  }, [language])

  const t = React.useCallback<LanguageContextValue["t"]>(
    (key, params) => {
      let value: string = dictionaries[language][key] ?? dictionaries.vi[key] ?? key
      if (params) {
        for (const [paramKey, paramValue] of Object.entries(params)) {
          value = value.replaceAll(`{${paramKey}}`, String(paramValue))
        }
      }
      return value
    },
    [language]
  )

  const toggleLanguage = React.useCallback(() => {
    setLanguage(language === "vi" ? "en" : "vi")
  }, [language, setLanguage])

  const value = React.useMemo(
    () => ({ language, setLanguage, toggleLanguage, t }),
    [language, setLanguage, toggleLanguage, t]
  )

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const value = React.useContext(LanguageContext)
  if (!value) throw new Error("useLanguage must be used inside LanguageProvider")
  return value
}

